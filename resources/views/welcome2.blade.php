        <h4>Мегакар Koenigsegg Jesko: 1600 «лошадей» и шесть сцеплений</h4>
        <p>
            Во-первых, пятилитровый битурбомотор V8 обзавелся новым «плоским» коленвалом — из-за этого пассивные опоры
            двигателя пришлось заменить активными. А два турбонагнетателя дополнены 20-литровым углепластиковым
            воздушным ресивером и небольшим электрическим компрессором — такая система позволяет почти полностью
            исключить турбозадержки. На 95-м бензине мотор выдает 1280 л.с., однако при использовании биотоплива Е85
            отдача вырастает до 1600 л.с. и 1500 Нм! Причем две трети максимального момента доступны в диапазоне от 2700
            до 6170 об/мин.
        </p>
        <p>
            Кузов мегакара Koenigsegg Jesko снабжен массой гидроцилиндров: они помогают открывать двери, передний и
            задний обтекатели, но главное — управлять активной аэродинамикой и избегать приседаний на заднюю ось при
            разгоне. Кстати, при скорости 250 км/ч на суперкар действует прижимная сила 800 кг, при 275 км/ч — одна
            тонна, а максимальное значение 1,4 т достигается при максимальной скорости, которую производитель для версии
            с развитым обвесом и антикрылом не сообщает. Однако модификация с упрощенной аэродинамикой, по расчетам,
            сможет разгоняться как минимум до 482 км/ч.
        </p>
    </main>
