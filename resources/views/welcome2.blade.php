        <h3>
            Koenigsegg Agera RS — гиперкар производства компании Koenigsegg. Эта модификация легче и мощнее базовой
            Agera. Agera RS был представлен общественности в 2015 году на выставке автомобилей в Женеве. Начальная
            стоимость автомобиля на данный момент примерно составляет 2 млн долларов США.</h3>
        <p>
            Agera RS оснащена алюминиевой пятилитровой V-образной восьмеркой с твин-турбонаддувом, который даёт 1,3 бара
            давлением наддува; четыре клапана на цилиндр (всего 32 клапана). Доступен вариант двигателя 1 МВт (1341
            лошадиная сила).
        </p>
        <p>
            Koenigsegg и Bugatti являются вечными соперниками в скоростях и на рынке. В 2017 году Bugatti выпускают свой
            новый автомобиль — Bugatti Chiron, который был заявлен якобы самым быстрым автомобилем в мире[4]. Также
            Bugatti назвали свои 41,96 секунды мировым рекордом[5], но спустя месяц Koenigsegg решили замерить свой
            Agera RS в упражнении 0-400-0 км/час, как и Bugatti замеряли свой Chiron. В первом заезде[6] Agera RS
            выполнила это упражнение за 36,44 секунды, тем самым уже сокрушив рекорд Bugatti. Затем, через месяц
            Koenigsegg вновь ставят новые рекорды, а именно: «самый быстрый серийный автомобиль» и «самый быстрый на
            общественном шоссе», к тому же, Agera RS удалось побить свой собственный рекорд в упражнении 0-400-0 км/час,
            поставив новый рекорд в 33,87 секунды.
        </p>
