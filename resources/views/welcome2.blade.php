        <h2>Koenigsegg Regera — автомобиль, выпущенный в 2017 году шведской компанией Koenigsegg под контролем Кристиана
            фон Кёнигсегга.</h2>
        <p>
            Максимальная скорость — 410 км/ч, разгон до 100 км/ч длится 2,8 секунды, вес автомобиля — 1590 кг. Гиперкар
            оснащен 5-литровым твин-турбо «V8», который развивает 1100 лошадиных сил и 1280 Нм крутящего момента при
            4100 оборотах в минуту (в диапазоне 2700-6170 оборотов момент достигает 1000 Нм).
        </p>
        <p>
            В силовую установку также входят три электромотора, разработанные фирмой YASA. Два из них расположены по
            одному в задних колесах (по 245 сил и 260 Нм каждый) и один на валу от ДВС (217 сил, 300 Нм). Для модели
            была разработана новая коробка передач, которая носит название Koenigsegg Direct Drive Transmission (KDD).
        </p>
        <p>
            Питает электромотор комплект литиевых батарей. Ёмкость батарей — 9,27 кВт*ч, объем 67 л, а масса — 115 кг.
            На электротяге гиперкар может проехать от 35 до 50 км. Батареи заряжаются с помощью системы рекуперации
            энергии при торможении, или от розетки.
        </p>
        <p>
            Автомобиль имеет задний привод, что немного ухудшает его управляемость по сравнению, например, с такими
            машинами, как Veyron или Chiron, но в то же время сильно снижает вес.
        </p>
    </main>
